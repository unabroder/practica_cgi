#!/bin/perl
use strict;
my ($n1,$n2) = (8, 4);

#operadores aritmeticos
#suma
my $suma = $n1 + $n2;
print "Suma: ".$suma."\n";
#resta
my $resta = $n1 - $n2;
print "resta: ".$resta."\n";
#multiplicacion
my $producto = $n1 * $n2;
print "multiplicacion: ".$producto."\n";
#division
my $div = $n2 / $n1;
print "Division: ".$div."\n";
#concatenacion
#exponenciacion **
my $exp = $n2 ** 2;
print $exp."\n";
#resto de una division %

#repeticion de caracteres x
my $rep = "Loco" x 5;
print $rep."\n";